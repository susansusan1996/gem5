# 清理舊的編譯結果
echo "清理舊編譯..."
scons --clean

# 重新編譯
echo "開始編譯..."
scons --build-type=fast -j4

# 檢查編譯結果
if [ $? -eq 0 ]; then
    echo ""
    echo "=========================================="
    echo "✓ 編譯成功！"
    echo "=========================================="
    ls -lh nvmain.fast
    echo ""
else
    echo ""
    echo "=========================================="
    echo "✗ 編譯失敗！請檢查錯誤訊息"
    echo "=========================================="
    exit 1
fi